<h1>Docke — водосточные системы Деке</h1>
<p>Водосточные системы Döcke (Дёке) — это очень популярный на европейских и российских рынках водосток, изготовленный из
    ПВХ материалов, поэтому имеет достаточно приемлемую цену. Несомненным плюсом является то факт, что изготавливают
    данные водостоки на Российском заводе «Дёке Экстружн».</p>
<p>Использование изготовленного по уникальной технологии поливинилхлорида в качестве исходного производственного
    материала — делает водосточные системы Дёке легкими, устойчивыми к внешней агрессивной среде и деформациям. Кроме
    того, транспортировка пластиковых водостоков Дёке до места сборки не составит проблем, так как вес устройства
    достаточно легкий. Монтаж можно осуществить согласно поставляющейся в комплекте инструкции, без участия
    профессиональных монтажников.</p>
<p>Несомненным преимуществом данных водостоков является широта их применения. Множество различных элементов
    изготавливаемых на Российском заводе «Дёке» позволяют организовывать отвод сточных вод с кровли любой сложности.
    Соединение элементов конструкции при монтаже происходит бесклеевым способом с использованием герметичных резиновых
    уплотнений.</p>
<p><strong>Водосточные системы «Дёке» неприхотливы</strong>: от вас потребуется это периодическая чистка желоба и трубы
    от листьев и другого скопившегося мусора. Цвета поставляемых водостоков ограничены, но все же представлены в
    достаточном разнообразии: белоснежный пломбир, благородный шоколад, солнечный банан, позитивный киви и сочный
    гранат.</p>
<h2>Пластиковые водосточные системы круглого сечения Деке</h2>
<p><img style="float: left; margin: 10px;" title="Дёке обладает небольшим весом - ее стоит купить"
        src="sites/default/files/img-vodostok/12.jpg" alt="Водосточная система Дёке проста в эксплуатации"/>  Водосточная
    система дома или наружный организованный водоотвод — наиболее популярный способ удаления осадков с кровельного
    покрытия. Водосточная система комплектуется из нескольких элементов. Для умеренных широт со средним количеством
    выпадения осадков организованный водоотвод, установленный по периметру здания, — лучший вариант. При этом нельзя
    сказать, что желоба и водосточные трубы портят внешний вид здания. Наоборот, подобранные со вкусом, они придают дому
    некую законченность и оживляют его, наделяя жильё уютом и признаками жизни. Водосточная система состоит из ряда
    элементов, некоторые из которых в различных дизайнерских решениях могут отсутствовать или принимать совершенно
    неожиданную форму. В любой системе водоотвода обязательны горизонтальные надстенные или подвесные желоба,
    вертикальные водосточные трубы и сливы, посредством которых вертикальные элементы водосточной системы соединяются с
    горизонтальными.</p>
<p>  Помимо этих элементов, водосточная система может быть укомплектована: тупиковыми (конечными) желобами; коленами для
    соединения желобов под различными углами; переходными коленьями для обхода карнизного выступа, которые либо
    формируются из нескольких жестких звеньев небольшой длины, либо выполняются целиком из гофрированного материала;
    дополнительными водосборниками, устанавливаемыми на водосточной трубе для сбора воды, поступающей из других
    источников; отметов для вывода воды из водосточной системы здания в систему дворовой канализации. Для крепления
    желобов в зависимости от их конструкции используют либо крюки и скобы для крепления надстенных желобов, либо
    лотковые скобы для крепления подвесных желобов, либо карнизные штыри для крепления водоприемных воронок. Водосточные
    трубы крепятся к стенам настенными штырями с хомутами.</p>
<p>  Горизонтальные желоба могут иметь различное поперечное сечение: полукруглое, квадратное или синусообразное. Кроме
    того, желоба и водосточные трубы изготавливаются нескольких размеров (в поперечном разрезе). Предпочтение того или
    иного размера зависит от уклона и общей площади крыши. Водосточные системы создаются из различных материалов. В
    настоящее время широко распространены пластиковые водосточные системы, стальные водосточные системы с покрытием
    различными типами полимеров, простые оцинкованные водосточные системы, водосточные системы из меди. Каждый из
    перечисленных типов имеет свои достоинства и недостатки. Выбор конкретного типа необходимо осуществлять учитывая
    регион применения, количество и тип осадков, годовые температуры, сложность конфигурации водосточной системы.</p>